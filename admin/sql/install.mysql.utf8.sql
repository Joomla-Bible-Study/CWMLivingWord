-- LivingWord v5.2.0 Schema

CREATE TABLE IF NOT EXISTS `#__livingword_plans` (
  `id` int UNSIGNED NOT NULL AUTO_INCREMENT,
  `alias` varchar(50) NOT NULL DEFAULT '' COMMENT 'URL-safe slug (e.g. comp, newtest, bio)',
  `title` varchar(200) NOT NULL DEFAULT '' COMMENT 'Display name or language key',
  `description` text NOT NULL COMMENT 'Plan description (may be language key)',
  `message` text NOT NULL COMMENT 'Introductory message (HTML or language key)',
  `audio` tinyint NOT NULL DEFAULT 0 COMMENT 'Enable audio player (0/1)',
  `audio_version` varchar(20) NOT NULL DEFAULT '' COMMENT 'BibleBrain fileset code',
  `testament` tinyint NOT NULL DEFAULT 0 COMMENT '0=full, 1=NT, 2=OT',
  `duration_type` varchar(20) NOT NULL DEFAULT 'annual' COMMENT 'annual, fixed, or self_paced',
  `total_days` int DEFAULT NULL COMMENT 'Total days for fixed/self_paced (NULL = auto from readings count)',
  `published` tinyint NOT NULL DEFAULT 0,
  `checked_out` int UNSIGNED DEFAULT NULL,
  `checked_out_time` datetime DEFAULT NULL,
  `ordering` int NOT NULL DEFAULT 0,
  `created` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `modified` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_alias` (`alias`),
  KEY `idx_published` (`published`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 DEFAULT COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `#__livingword_plans_details` (
  `id` int UNSIGNED NOT NULL AUTO_INCREMENT,
  `plan_id` int UNSIGNED NOT NULL DEFAULT 0 COMMENT 'FK to plans.id',
  `ordering` int NOT NULL DEFAULT 0 COMMENT 'Day number within the plan',
  `reading` varchar(255) NOT NULL DEFAULT '' COMMENT 'Human-readable passage (e.g. Genesis 1-3; Psalm 23)',
  `audio` varchar(255) NOT NULL DEFAULT '' COMMENT 'Optional audio URL override',
  `descrip` text NOT NULL DEFAULT ('') COMMENT 'Optional reading description',
  PRIMARY KEY (`id`),
  KEY `idx_plan_id` (`plan_id`),
  KEY `idx_plan_ordering` (`plan_id`, `ordering`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 DEFAULT COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS `#__livingword_users` (
  `id` int UNSIGNED NOT NULL AUTO_INCREMENT,
  `user_id` int NOT NULL DEFAULT 0 COMMENT 'FK to #__users.id',
  `plan_id` int UNSIGNED NOT NULL DEFAULT 0 COMMENT 'FK to plans.id',
  `bible_version` varchar(20) NOT NULL DEFAULT '' COMMENT 'Translation code (e.g. kjv)',
  `audio_version` varchar(20) NOT NULL DEFAULT '' COMMENT 'Preferred audio Bible fileset code',
  `email` tinyint NOT NULL DEFAULT 0 COMMENT 'Receive daily reading email (0/1)',
